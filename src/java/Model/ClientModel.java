package Model;

/**
 *
 * @author zakaria
 */
public class ClientModel {

    private int id;
    private String fname;
    private String lname;
    private String address;
    private String phone;

    public ClientModel(String fname, String lname, String address, String phone) {
        super();
        this.fname = fname;
        this.lname = lname;
        this.address = address;
        this.phone = phone;
    }

    public ClientModel(int id, String fname, String lname, String address, String phone) {
        super();
        this.id = id;
        this.fname = fname;
        this.lname = lname;
        this.address = address;
        this.phone = phone;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the fname
     */
    public String getFname() {
        return fname;
    }

    /**
     * @param fname the fname to set
     */
    public void setFname(String fname) {
        this.fname = fname;
    }

    /**
     * @return the lname
     */
    public String getLname() {
        return lname;
    }

    /**
     * @param lname the lname to set
     */
    public void setLname(String lname) {
        this.lname = lname;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

}
